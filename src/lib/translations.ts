export const translateStation = (name: string, lang: Language): string => {
  if (lang === "en") return name;
  return stationNamesZh[name] || name;
};

export const translateLine = (lineName: string, lang: Language): string => {
  if (lang === "en") return lineName;
  if (lineName === "LRT Kelana Jaya Line") return "轻快铁格拉那再也线";
  if (lineName === "LRT Ampang Line") return "轻快铁安邦线";
  if (lineName === "LRT Sri Petaling Line") return "轻快铁大城堡线";
  if (lineName === "MRT Kajang Line") return "捷运加影线";
  if (lineName === "MRT Putrajaya Line") return "捷运布城线";
  if (lineName === "KL Monorail Line") return "吉隆坡单轨线";
  if (lineName === "BRT Sunway Line") return "快捷巴士双威线";
  if (lineName === "LRT Shah Alam Line") return "轻快铁莎阿南线";
  return lineName;
};
